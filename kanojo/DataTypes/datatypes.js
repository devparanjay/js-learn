//DATA Types

//Primitive Data types

//string
const baka = "Alex";
console.log(baka);
console.log(typeof baka);

//Number
const age = 20;
console.log(age);
console.log(typeof age);

//Bool
const isdumb = true;
console.log(isdumb);
console.log(typeof isdumb);

//Null
const flaws = null;
console.log(flaws);
console.log(typeof flaws); //object

//Undefined
let stat;
console.log(stat);
console.log(typeof stat);

//Symbol
const sym = Symbol();
console.log(sym);
console.log(typeof sym);

//Reference datatypes- objects

//Array
const hobbies = ["Reading", "Sleeping"];
console.log(hobbies);
console.log(typeof hobbies);

//Onject literal
const address = {
  addr: "Inferno"
}

console.log(address);
console.log(typeof addr);
console.log(typeof address);

const today = new Date(); //date constructor called
console.log(today);
console.log(typeof today);
