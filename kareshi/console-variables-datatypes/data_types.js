// Data Types in JavaScript

// Primitive Data Types

// String
const kanOjo = "Sanika";
// Logging to Console
console.log(kanOjo);
// Logging Data Type to Console
console.log(typeof kanOjo);

// Number
const san = 8;
// Logging to Console
console.log(san);
// Logging Data Type to Console
console.log(typeof san);

// Boolean
const lovesMe = true;
// Logging to Console
console.log(lovesMe);
// Logging Data Type to Console
console.log(typeof lovesme);

// NULL
const kids = null;
// Logging to Console
console.log(kids);
// Logging Data Type to Console
console.log(typeof kids);

// Undefined
let numberOfKids;
// Logging to Console
console.log(numberOfKids);
// Logging Data Type to Console
console.log(typeof numberOfKids);

// Symbols
const sYmBoL = Symbol()
// Logging to Console
console.log(sYmBoL);
// Logging Data Type to Console
console.log(typeof sYmBoL);


// Reference Data Types - Objects

// Array
const meLikes = ['Kanojo', 'My Kanojo'];
// Logging to Console
console.log(meLikes);
// Logging Data Type to Console
console.log(typeof meLikes);

// Object Literal
const sheLikes = {
    'From Kareshi': 'Kissies',
    'Ice Cream': 'Butterscotch'
}
// Logging to Console
console.log(sheLikes);
// Logging Data Type to Console
console.log(typeof sheLikes);

// Date
const now = new Date();
// Logging to Console
console.log(now);
// Logging Data Type to Console
console.log(typeof now);