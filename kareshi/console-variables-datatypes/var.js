// Using var.

/*
    *****
    *var*
    *****
*/

// Storing a var and logging it.
var kanojo = "Sanika";
console.log(kanojo);

// Changing the var.
kanojo = "Sweetheart";
console.log(kanojo)

// Initiation the var.
var sweetheart;

// var is undefined.
console.log(sweetheart);

// Defining the var.
sweetheart = kanojo;
console.log(kanojo);


// Naming Conventions

// Camel Case
var sweetiePie = kanojo;

// Underscore
var sweetie_pie = kanojo;

// Pascal Case
var SweetiePie = kanojo;